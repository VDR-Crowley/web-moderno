// CÓDIGO NÃO EXECUTAVEL

// paradigma procedural
function processos(valor1, valor2, valor3) {
  return valor1 + valor2 + valor3;
}
processos(1, 2, 3);


// paradigma OO
objeto = {
  valor1,
  valor2,
  valor3,
  processos(){
    // ...
  }
};
objeto.processos() // o foco passou a ser o objeto

// principios importantes:
// 1. abstração
// 2. encapsulamento
// 3. herança (prototype)
// 4. polimorfismo